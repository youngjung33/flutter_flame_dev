  }

  /** [type] 1~7, [rotation] 0~3에 해당하는 4x4 패턴 반환. */
  static List<List<int>> getPattern(int type, int rotation) {
    final patternIndex = (type - 1) * 4 + rotation;
    return patterns[patternIndex] ?? patterns[0]!;
  }
